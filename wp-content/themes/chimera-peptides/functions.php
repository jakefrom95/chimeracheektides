<?php
/**
 * Chimera Peptides theme functions and definitions
 */

if ( ! function_exists( 'chimera_peptides_setup' ) ) :
    function chimera_peptides_setup() {
        // Add default posts and comments RSS feed links to head.
        add_theme_support( 'automatic-feed-links' );

        // Let WordPress manage the document title.
        add_theme_support( 'title-tag' );

        // Enable support for Post Thumbnails on posts and pages.
        add_theme_support( 'post-thumbnails' );

        // Register navigation menus
        register_nav_menus( array(
            'primary' => esc_html__( 'Primary Menu', 'chimera-peptides' ),
        ) );

        // Switch default core markup for search form, comment form, and comments to output valid HTML5.
        add_theme_support( 'html5', array(
            'search-form',
            'comment-form',
            'comment-list',
            'gallery',
            'caption',
        ) );
    }
endif;
add_action( 'after_setup_theme', 'chimera_peptides_setup' );

/**
 * Enqueue scripts and styles.
 */
function chimera_peptides_scripts() {
    wp_enqueue_style( 'chimera-peptides-style', get_stylesheet_uri(), array(), '1.0.0' );
    wp_enqueue_style( 'chimera-peptides-main', get_template_directory_uri() . '/assets/css/main.css', array(), '1.0.0' );
    
    // Add Google Fonts (Inter)
    wp_enqueue_style( 'google-fonts', 'https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&display=swap', array(), null );

    wp_enqueue_script( 'chimera-peptides-navigation', get_template_directory_uri() . '/assets/js/navigation.js', array(), '1.0.0', true );
}
add_action( 'wp_enqueue_scripts', 'chimera_peptides_scripts' );
